#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	long long	sign;
	long long	res;

	sign = 1;
	res = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
		if (res * sign > 2147483647)
			return (-1);
		if (res * sign < -2147483648)
			return (0);
	}
	return ((int)(res * sign));
}
